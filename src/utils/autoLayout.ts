import dagre from 'dagre';
import { Node, Edge, Position } from 'reactflow';

/**
 * Lays out nodes and edges using Dagre for a Directed Acyclic Graph (DAG).
 * 
 * @param nodes Current nodes in the flow
 * @param edges Current edges in the flow
 * @param options layout options (direction)
 * @returns Object containing the layouted nodes and edges
 */
export const getLayoutedElements = (
    nodes: Node[],
    edges: Edge[],
    options: { direction: 'TB' | 'LR' } = { direction: 'LR' }
) => {
    const dagreGraph = new dagre.graphlib.Graph();
    dagreGraph.setDefaultEdgeLabel(() => ({}));

    const isHorizontal = options.direction === 'LR';
    dagreGraph.setGraph({ rankdir: options.direction });

    // Node dimensions (default fallback if not measured yet)
    // n8n nodes are roughly wide rectangular cards
    const nodeWidth = 250;
    const nodeHeight = 80;

    nodes.forEach((node) => {
        dagreGraph.setNode(node.id, { width: nodeWidth, height: nodeHeight });
    });

    edges.forEach((edge) => {
        dagreGraph.setEdge(edge.source, edge.target);
    });

    dagre.layout(dagreGraph);

    const layoutedNodes = nodes.map((node) => {
        const nodeWithPosition = dagreGraph.node(node.id);

        // We are shifting the dagre node position (anchor=center center) to the top left
        // so it matches React Flow's anchor point
        node.targetPosition = isHorizontal ? Position.Left : Position.Top;
        node.sourcePosition = isHorizontal ? Position.Right : Position.Bottom;

        // We modify the node's position directly
        // Ideally we return a new object to avoid mutation issues, map does that shallowly for the array
        return {
            ...node,
            position: {
                x: nodeWithPosition.x - nodeWidth / 2,
                y: nodeWithPosition.y - nodeHeight / 2,
            },
        };
    });

    return { nodes: layoutedNodes, edges };
};
